# there are 4 basic types of variables

# str (strings)
"Hi!"  # a string is denoted with a ""

# int (integers, numbers with no decimals)
5

# float (numbers with decimals)
10.532

# bool (booleans, true / false)
True  # or False,
# * Note: python is case sensitive so "True" (excluding the "") is the exact syntax that must be used and not "true"

# none (the absence of a value)
None
