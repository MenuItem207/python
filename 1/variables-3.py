# to create a variable and assign it a value:
test_variable = "Hello!"

# use the value
print(test_variable)

# reassign (update the value) of the variable
test_variable = "new value!!"
print(test_variable)

# you can change the type of the variable too!
test_variable = 15
print(test_variable)

# assign another variable the value of the first variable
test_variable_2 = test_variable

print(test_variable_2)

# * note: in MOST languages, variables can only store one type but in python, the variables can store ANY type

